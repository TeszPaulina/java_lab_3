public class Student {
    String imie;
    String nazwisko;
    int wiek;
    boolean czyAktywny;

    public Student() {
    }

    public Student(String imie, String nazwisko, int wiek, boolean czyAktywny) {
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.wiek = wiek;
        this.czyAktywny = czyAktywny;
    }
}
