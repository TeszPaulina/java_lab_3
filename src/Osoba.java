public class Osoba {

    String imie;
    String nazwisko;
    int pieniadze;

    public Osoba() {
        this.imie = "";
        this.nazwisko = "";
        this.pieniadze = 0;
    }

    public Osoba(String imie, String nazwisko, int pieniadze) {
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.pieniadze = pieniadze;
    }
}
